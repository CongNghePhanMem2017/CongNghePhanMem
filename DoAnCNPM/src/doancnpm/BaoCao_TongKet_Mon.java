    private void jButton_LapBaoCaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_LapBaoCaoActionPerformed
       
        String namhoc = "";
        namhoc = jComboBox_NamHoc.getSelectedItem().toString();
        String monhoc = "";
        monhoc = jComboBox_MonHoc.getSelectedItem().toString();
        String hocky = "";
        hocky = jComboBox_HocKy.getSelectedItem().toString();
         try {
            DB.conn = DriverManager.getConnection(DB.dbURL);
            // câu lệnh xem dư liệu
            String DiemTongKetNamHoc = " exec BaoCaoTongKetMon '"+namhoc+"' ,'"+monhoc+"', '"+hocky+"'";
            // tạo đối tượng thực thi câu lênh select
            DB.st = DB.conn.createStatement();
            // Thực thi
            DB.rs = DB.st.executeQuery(DiemTongKetNamHoc); 
             Vector data = null;
            tblMode_BaoCao.setRowCount(0);
             // Nếu sách không tồn tại
            if (DB.rs.isBeforeFirst() == false) {
               JOptionPane.showMessageDialog(this, "Không có hs ở lớp nào đạt hết!");
              // return; 
            }
            tblMode_BaoCao.setRowCount(0); 
            while (DB.rs.next()) {
                data = new Vector();
                data.add(DB.rs.getString("TenLop"));
                data.add(DB.rs.getString("SiSo"));
                data.add(DB.rs.getString("sldat"));
                data.add(DB.rs.getString("tile")); 
                // Thêm một dòng vào table model
                tblMode_BaoCao.addRow(data);
            }
            jTable_BaoCao.setModel(tblMode_BaoCao); // thêm dữ liệu vào table 
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Không có dự liệu");
        } finally {
            try {
                if (DB.conn != null) {
                    DB.conn.close();
                }
                if (DB.st != null) {
                    DB.st.close();
                }
                if (DB.rs != null) {
                    DB.rs.close();
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
         // ds học sinh chưa đạt
         try {
            DB.conn = DriverManager.getConnection(DB.dbURL);
            // câu lệnh xem dư liệu
            String DiemTongKetNamHoc_ChuaDat = " exec BaoCaoTongKetMon_ChuaDat '"+namhoc+"' ,'"+monhoc+"', '"+hocky+"'";
            // tạo đối tượng thực thi câu lênh select
            DB.st = DB.conn.createStatement();
            // Thực thi
            DB.rs = DB.st.executeQuery(DiemTongKetNamHoc_ChuaDat);
          
             Vector data = null;
            tblMode_BaoCao_ChuaĐat.setRowCount(0);
             // Nếu sách không tồn tại
            if (DB.rs.isBeforeFirst() == false) {
               JOptionPane.showMessageDialog(this, "Tất cả HS Đều Đạt 100%!"); 
               return ;
            }
            tblMode_BaoCao_ChuaĐat.setRowCount(0); 
            while (DB.rs.next()) {
                data = new Vector();
                data.add(DB.rs.getString("TenLop"));
                data.add(DB.rs.getString("SiSo"));
                data.add(DB.rs.getString("slchuadat"));
                data.add(DB.rs.getString("tile")); 
                // Thêm một dòng vào table model
                tblMode_BaoCao_ChuaĐat.addRow(data);
            }
            jTable_BaoCao_ChuaDat.setModel(tblMode_BaoCao_ChuaĐat); // thêm dữ liệu vào table 
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi Lập báo cáo");
        } finally {
            try {
                if (DB.conn != null) {
                    DB.conn.close();
                }
                if (DB.st != null) {
                    DB.st.close();
                }
                if (DB.rs != null) {
                    DB.rs.close();
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
   
    }//GEN-LAST:event_jButton_LapBaoCaoActionPerformed
